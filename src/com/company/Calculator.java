package com.company;

public class Calculator {

    int firstNumber;
    int secondNumber;

    public int add() {
        return firstNumber + secondNumber;
    }

    public int multiply() {
        return firstNumber * secondNumber;
    }

    public int divide() {
        return firstNumber / secondNumber;
    }

    public int takeAway() {
        return firstNumber - secondNumber;
    }
}
